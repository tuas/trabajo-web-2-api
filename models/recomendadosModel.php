<?php
include_once ("models/models.php");
///////////////////////////////////////////////////////////////////////////////
////////////////*MANEJO DE TABLA RECOMENDADO Y IMAGENES RECOMENDADO*////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
class recomendadosmodelo extends Model{


  function __construct(){
    parent::__construct();

  }

  ///////////////////////////////////////////////////////////////////////////////
  ////////////////*CONSULTA TABLA RECOMENDADO*////////////////////////////////////
  ///////////////////////////////////////////////////////////////////////////////

  function getservicios(){
    $posible = $this->db->prepare( "SELECT * FROM recomendados");
    $posible->execute();
    return $posible->fetchall(PDO::FETCH_ASSOC);
  }

  ///////////////////////////////////////////////////////////////////////////////
  ////////////////*CONSULTA TABLA IMAGENES RECOMENDADO*////////////////////////////////////
  ///////////////////////////////////////////////////////////////////////////////

  function getimagenes(){
    $posible = $this->db->prepare( "SELECT * FROM imagenes_recomendadas");
    $posible->execute();
    return $posible->fetchall(PDO::FETCH_ASSOC);
  }


}


